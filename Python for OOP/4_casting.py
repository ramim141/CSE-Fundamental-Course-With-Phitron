x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0



# Integer Casting
x_int = int(3.5)  # x_int will be 3

# Float Casting
x_float = float("3.5")  # x_float will be 3.5

# String Casting
x_str = str(3.5)  # x_str will be "3.5"

# Boolean Casting
x_bool_1 = bool(0)  # x_bool_1 will be False
x_bool_2 = bool(1)  # x_bool_2 will be True

# List Casting
x_list_1 = list((1, 2, 3))  # x_list_1 will be [1, 2, 3]
x_list_2 = list("hello")    # x_list_2 will be ['h', 'e', 'l', 'l', 'o']

# Printing the results
print("Integer Casting:", x_int)
print("Float Casting:", x_float)
print("String Casting:", x_str)
print("Boolean Casting 1:", x_bool_1)
print("Boolean Casting 2:", x_bool_2)
print("List Casting 1:", x_list_1)
print("List Casting 2:", x_list_2)
